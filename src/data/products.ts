function createSkinDetails(
  productType: string,
  condition: string,
  floatValue?: string,
  game = "Counter-Strike 2",
): ProductDetails {
  return {
    specifications: [
      { label: "Игра", value: game },
      { label: "Тип", value: productType },
      { label: "Состояние", value: condition },
      ...(floatValue ? [{ label: "Float", value: floatValue }] : []),
    ],
    fulfillment: {
      title: "Передача через Steam",
      description: "Предмет передается через предложение обмена Steam.",
      requirements: [
        "Для получения предмета требуется вход через Steam.",
        "Укажите действующий Steam Trade URL перед оформлением заказа.",
      ],
    },
  };
}

